let a = 10;

// Relacionales
console.log(a > 5); // true
console.log(a < 5); // false
console.log(a >= 5); // true
console.log(a <= 5); // false

// Igualdad
console.log(a == 5); // false
console.log(a != 5); // true
console.log(a === 10); // true
console.log(a === '10'); // false
console.log(a !== '10'); // true
console.log(a !== 10); // false
